#!/usr/bin/env node

// Embeds ini-grammar.jsonic into go/grammar_text.go as a Go raw string constant.
// Run via: npm run embed-go

const fs = require('fs')
const path = require('path')

const grammarFile = path.join(__dirname, 'ini-grammar.jsonic')
const goFile = path.join(__dirname, 'go', 'grammar_text.go')

const grammar = fs.readFileSync(grammarFile, 'utf8')

// Go raw strings (backtick-delimited) cannot contain backticks.
if (grammar.includes('`')) {
  console.error('Error: grammar file contains backticks, cannot embed in Go raw string')
  process.exit(1)
}

const goContent = `// Code generated by embed-grammar-go.js from ini-grammar.jsonic. DO NOT EDIT.

package ini

const grammarText = \`
${grammar}\`
`

fs.writeFileSync(goFile, goContent)
